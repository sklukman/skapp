package com.sk.java_8;

public class Info {
	String name;
	int age;

	public Info(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	@Override
	public String toString() {
		return "Info [name=" + name + ", age=" + age + "]";
	}
}
