package com.sk.logical;

import java.util.Scanner;

public class OneToNNumber {

	public static void main(String[] args) {
		Scanner scn=new Scanner(System.in);
		System.out.println("Enter a No");
		int n=scn.nextInt();
		for(int i=1;i<=n;i++)
		{
			System.out.println(i);
		}

	}

}
